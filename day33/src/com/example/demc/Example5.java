package com.example.demc;


public class Example5 {
	public static void main(String[] args) {
		//작은 따옴표는 글자 1개 > char > 거의 쓸모가 없다
		char ch = 'A';
		
		// 문자열은 큰 따옴표로 감싸고 객체
		// 자바에서 유일하게 new를 사용하지 않고 객체를 생성
		String str = "홍길동";
		
		// 참 거짓
		// JS나 파이썬은 거짓과 없는 값, 참과 있는 값이 변환된다
		// 자바에서 boolean은 다른 타입으로 변환 불가능
		boolean a = 5>3;
	}
}
